//'3' si un numero se encuntra asi js lo va a leer igual
//--Operador igual (==)

console.log(3==3);//Esta operacion dira que es true
console.log(3==2);//Esta operacion dira que es false


//--Operador no es igual (!=)
console.log(3 != 3);

//Operador estrictamente igual (===)
console.log(3===3);

//operador de igualdad estricta (!==)
console.log(3!==3);

//Operadores mayor que >, mayor igual que >=, menor que <, menor igual que <=
console.log(3>2);//true
console.log(3>=3);//true
console.log(2<4);//true
console.log(2<=2);//true