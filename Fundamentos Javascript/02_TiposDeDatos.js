//--Numero--Number
console.log(23); //Entero
console.log(22.22); //Desimal

//--Texto--String
console.log("Erick Medina");
console.log("Jose Medina");
console.log(`Hola, soy Erik`);

//--Logicos--Booleans
console.log(true); //Verdader
console.log(false); //Falso

//--Indefinido--undefine
console.log(undefined);

//--null
console.log(null);

//--Tipos de datos estructurales
//--Objeto--object
console.log({ nombre: "Erick", apellido: "Medina", edad: 22 });

//--Arreglos--Array--(Lista)
console.log(["Mexico", "USA", "Brazul"]);
